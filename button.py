class button :

	def __init__(self):
		#konstruktor
		self.jk = ""
		self.lamatinggal = ""
		self.status = ""

		#1
	def setJk(self, jk):
		#mengeset nilai jeniskelamin
		self.jk = jk

	def getJk(self):
		#mengembalikan nilai jeniskelamin
		return self.jk

		#2
	def setLamaTinggal(self, lamatinggal):
		#mengeset nilai lama tinggal
		self.lamatinggal = lamatinggal

	def getLamaTinggal(self):
		#mengembalikan nilai lama tinggal
		return self.lamatinggal

		#3
	def setStatus(self, status):
		#mengeset nilai status
		self.status = status

	def getStatus(self):
		#mengembalikan nilai status
		return self.status

