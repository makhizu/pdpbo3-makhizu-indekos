class field :

	def __init__(self):
		#konstruktor
		self.nama = ""
		self.no_telp = ""
		self.profesi = ""

		#1
	def setNama(self, Nama):
		#mengeset nilai nama
		self.nama = Nama

	def getNama(self):
		#mengembalikan nilai nama
		return self.nama

		#2
	def setNo_telp(self, no_telp):
		#mengeset nilai no_telp
		self.no_telp = no_telp

	def getNo_telp(self):
		#mengembalikan nilai no_telp
		return self.no_telp

		#3
	def setProfesi(self, profesi):
		#mengeset nilai profesi
		self.profesi = profesi

	def getProfesi(self):
		#mengembalikan nilai profesi
		return self.profesi

